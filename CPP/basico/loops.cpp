#include<iostream>
int main(){
    int x =0;
    int y = 0;
    std::cout<<"while loop"<<std::endl;
    while(x<=10){
        std::cout<<"oi"<<std::endl;
        x++;
    }
    std::cout<<"for loop"<<std::endl;
    for(int y=0;y<5;y++){
        std::cout<<"eai"<<std::endl;
    }
    return 0;
}